#include "libft.h"
#include <stdint.h>

void *ft_calloc(size_t count, size_t size)
{
    void *s;

    if (size != 0 && count > SIZE_MAX / size)
        return NULL;
    s = malloc(count * size);
    if (!s)
        return NULL;
    ft_bzero(s, count * size);
    return s;
}

#include <stdio.h>
int main(void)
{
	size_t i;
	int *tab;

	tab = ft_calloc(5, sizeof(int));
	for (i = 0; i < 5; i++)
		printf("%d\n", tab[i]);
	free(tab);
	return (0);
}
